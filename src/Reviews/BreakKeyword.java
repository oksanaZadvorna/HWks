package Reviews;

public class BreakKeyword {
    public static void main(String[] args) {
        for(int i=1; i<=5; i++){
            if(i==3){
                break; //will exit the code when reached to #3
            }


            System.out.println(i);
            System.out.println("Hi");
            System.out.println("Bye");
        }
    }
}

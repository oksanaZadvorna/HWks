package class6;

public class SwitchCase3 {
    public static void main(String[] args) {
        String name = "John";
        switch (name){
            case "John":
                System.out.println("Chicago");
                break;
            case "Merry":
                System.out.println("DC");
                break;
            case "Olga":
                System.out.println("Frankivsk");
                break;
        }
    }
}

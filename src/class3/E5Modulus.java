package class3;

public class E5Modulus {
    public static void main(String[] args) {
        int num1=3;
        int num2=4;
        System.out.println(num1/num2);
        System.out.println(num1%num2);

        int noOfSlices=5;
        int noOfIndividuals=3;
        System.out.println(noOfSlices/noOfIndividuals);
        System.out.println(noOfSlices%noOfIndividuals);
    }
}
